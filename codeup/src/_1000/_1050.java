import java.util.Scanner;

//[기초-비교연산] 두 정수 입력받아 비교하기2
public class _1050 {
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		int a = sc.nextInt(); 
	
		int b = sc.nextInt(); 
		

		if(a == b) {
			System.out.println(1);
		}
		else
			System.out.println(0);
		
	}
	
}
