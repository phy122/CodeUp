package _1000;

import java.util.Scanner;

public class _1045 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		float a = sc.nextInt();
		float b = sc.nextInt();	
		
		System.out.printf("%.0f",a+b);
		System.out.println();
		System.out.printf("%.0f",a-b);
		System.out.println();
		System.out.printf("%.0f",a*b);
		System.out.println();
		System.out.printf("%.0f",a/b);
		System.out.println();
		System.out.printf("%.0f",a%b);
		System.out.println();
		System.out.printf("%.2f",a/b);
		
		
	}

}
